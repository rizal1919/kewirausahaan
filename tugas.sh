customer relationship, ialah Menjaga customer relationship (hubungan pelanggan) yang baik akan menguntungkan bisnis. Cara usaha Anda menjalin hubungan dengan pelanggan akan sangat berdampak pada penjualan dan image bisnis Anda secara keseluruhan.

	c.relationship
	-1 menyimpan kontak media sosial pelanggan (wa,ig)
	-2 menyediakan contact person yang aktif 24 jam


key activities ialah kegiatan utama operasional perusahaan. Misalnya untuk Starbucks, penggilingan kopi, pembuatan kopi, kegiatan R&D, hingga kegiatan promosi per harinya 

	k.activities
	-1 pembungkusan barang untuk kado
	-2 kegiatan R&D 
	-3 melakukan promosi setiap harinya

Key partnership (mitra utama) adalah daftar perusahaan/pemasok/pihak eksternal lain yang mungkin Anda perlukan untuk menjalankan bisnis dan memberikan nilai kepada pelanggan. Contohnya seperti pemasok bahan baku makanan, penyedia peralatan masak, dan vendor yang memproduksi kemasan makanan.

	k.partnership
	-1 vendor pemasok bahan pembungkus kado
	-2 supplier aksesoris tambahan kado
	-3 distributor umum (jnt,jne,anteraja)

